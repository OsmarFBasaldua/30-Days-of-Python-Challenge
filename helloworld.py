# Day 1 - 30 Days of Python Challenge

print("Hello, World!") # Printing a string - My first Python code!

print(2+3) # Addition (+)
print(3-1) # Subtraction (-)
print(2*3) # Multiplication (*)
print(3/2) # Division (/)
print(3 ** 2) # Exponentiation (**)
print(3 % 2) # Modulus (%)
print(3 // 2) # Floor division operator (//)

# Checking data types
print(type(10)) # int
print(type(3.14)) # float
print(type(1 + 2j)) # complex number
print(type('Osmar')) # string
print(type([1, 2, 3])) # list
print(type({'name': 'Osmar', 'age': 30})) # dictionary
print(type({9.8, 3.14, 2.7})) # set
print(type((9.8, 3.14, 2.7))) # tuple